// Let's set a bit and flow!
// I came, I divided, I conquered!

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    int r, h;
    cin >> r >> h;

    int ans = h / r * 2;
    h %= r;

    if (4ll * h * h >= 3ll * r * r) ans += 3;
    else if (h * 2 >= r) ans += 2;
    else ans ++;

    cout << ans;

    return 0;
}